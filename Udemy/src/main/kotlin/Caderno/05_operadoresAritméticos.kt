package Caderno

fun main() {
    /*
    + soma
    - subtração
    * multiplicação
    / divisão
    % resto
     */

    var idade = 30
    idade = idade + 1
    idade -= 1 //versão simplificada
    idade *= 1
    idade /= 1
    idade %= 4 //quando colorido, não há resto.

    // outra forma de somar e subtrair:
    idade++ //usa a variável e depois incrementa
    ++idade //incrementa e depois usa a variável
    idade--
    --idade
}